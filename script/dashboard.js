document.querySelector("#select").addEventListener("change", function () {
  if (this.value === "pending") {

    let paragraph = document.querySelector("#paragraph")
    paragraph.innerText = "You Dont have any Pending Applications";
  }
  else if (this.value === "rejected") {

    let paragraph = document.querySelector("#paragraph")
    paragraph.innerText = "You Dont have any Rejected Applications";
  }
  else if (this.value === "shortlisted") {

    let paragraph = document.querySelector("#paragraph")
    paragraph.innerText = "You Dont have any Shortlisted Applications";
  }
  else if (this.value === "rejected after") {

    let paragraph = document.querySelector("#paragraph")
    paragraph.innerText = "You Dont have any Rejected After Shortlisting Applications";
  }
  else if (this.value === "hired") {

    let paragraph = document.querySelector("#paragraph")
    paragraph.innerText = "You Dont have any Hired Applications";
  }
  else if (this.value === "join") {

    let paragraph = document.querySelector("#paragraph")
    paragraph.innerText = "You Dont have any Join Applications";
  }
  else if (this.value === "didjoin") {

    let paragraph = document.querySelector("#paragraph")
    paragraph.innerText = "You Dont have any Did Not Join Applications";
  }
  else {
    paragraph.innerText = "You Dont Have Any Applications";
  }

});

